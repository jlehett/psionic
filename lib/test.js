/**
 * Returns the value of `a` + `b`.
 *
 * @example
 * addNumbers(2, 4) // Returns 6
 *
 * @param {number} a The first number in the summation
 * @param {number} b The second number in the summation
 * @returns {number} The 2 numbers added together
 */
function addNumbers(a, b) {
    return a + b;
}